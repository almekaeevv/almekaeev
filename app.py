def calc(a, b):
    # Если одно из чисел 0, возвращаем "Операция невозможна"
    if a == 0 or b == 0:
        return "Операция невозможна"
    
    # Если оба числа четные, возвращаем их сумму
    if a % 2 == 0 and b % 2 == 0:
        return a + b
    
    # Если хотя бы одно число нечетное, возвращаем их разность
    return a - b

# Примеры:
print(calc(4, 6))  # Ответ: 10
print(calc(3, 5))  # Ответ: -2
print(calc(0, 5))  # Ответ: Операция невозможна
